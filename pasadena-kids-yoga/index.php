		<p class="lead">Pasadena Kid’s Yoga began in 2015, as a vision that Pili Bailey, Pasadena Kid’s Yoga founder and creator, had to bring Yoga to kids from all races, ethnic groups, and socioeconomic backgrounds. After Pili became a mother for the first time in 2013, she began to feel the pull to get back to her center through her practice of yoga. This lead Pili to attend a 6 month long YogaWorks 200 hour teacher training in 2015, and to train to teach kids yoga through miniyogis&trade; also in 2015.  Pili began teaching yoga to moms with tots through Fuller Seminary, and also taught kid’s yoga with friends at various neighborhood parks. As her son, now aged 4, grew, Pili incorporated yoga into their times together.  She saw how it inspired her son to have confidence in his own body, to increase his strength, and to gain a greater sense of boldness in navigating changes in his environment. As a science minded individual, with a Masters in Chemistry from Georgetown University, Pili keeps her knowledge current by studying scientific literature confirming the positive effects of yoga on the developing brain.</p>
